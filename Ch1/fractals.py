def rotate_scale(theta,scale,x):
  rotation_matrix=np.array([[np.cos(theta),-np.sin(theta)],[np.sin(theta),np.cos(theta)]])
  r=x[1]-x[0]
  r = (x[1] - x[0]) @ rotation_matrix.T
  r=r*scale
  r=r+x[0]
  return r


